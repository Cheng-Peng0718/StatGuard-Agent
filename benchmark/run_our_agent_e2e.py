"""
End-to-end benchmark runner -- FULL AGENT (LLM routing included).

Unlike run_our_agent.py (which calls plugins directly and is fully
deterministic), this script drives the complete LangGraph pipeline:

    natural-language question -> LLM supervisor routes -> plugin executes
    -> summarize -> report

This is the fairest comparison surface against Julius / ChatGPT ADA /
Auto-Analyst, which are also natural-language-in, report-out. It also
exercises the core thesis: the LLM only ROUTES; the statistics are computed
by deterministic plugins.

Requirements:
  - OPENAI_API_KEY in the environment (the supervisor uses gpt-4o).
  - The trap datasets generated by benchmark/generate_datasets.py.

Caveat: because an LLM chooses the tool, results can vary slightly run to run
(tool selection is not perfectly deterministic even at temperature 0). Run
each case a few times if you need a stable score. This variance is itself a
useful contrast: our STATISTICS are deterministic; only the routing is not.
"""

from __future__ import annotations

import json
import os
import shutil
import sys
import tempfile
import uuid
from typing import Any, Dict, List

import pandas as pd

from benchmark.rubric import RUBRICS


DATASETS_DIR = os.path.join(os.path.dirname(__file__), "datasets")
RESULTS_DIR = os.path.join(os.path.dirname(__file__), "results")
os.makedirs(RESULTS_DIR, exist_ok=True)

MAX_STEPS = 12


# ============================================================
# Preconditions
# ============================================================

def _require_api_key() -> bool:
    if not os.environ.get("OPENAI_API_KEY"):
        print("=" * 72)
        print("OPENAI_API_KEY is not set.")
        print("The end-to-end benchmark drives the real LLM supervisor (gpt-4o),")
        print("so it needs an OpenAI key. Set it and re-run:")
        print()
        print('    export OPENAI_API_KEY="sk-..."   # macOS/Linux')
        print('    $env:OPENAI_API_KEY="sk-..."     # PowerShell')
        print()
        print("Tip: the deterministic plugin-layer benchmark (run_our_agent.py)")
        print("needs no API key and already scores the statistical rigor directly.")
        print("=" * 72)
        return False
    return True


# ============================================================
# Per-case natural-language prompts
# ============================================================
# These are intentionally phrased the way a real researcher would ask --
# NOT naming the test. The agent must choose the right test itself. This is
# exactly what we will type into Julius / ChatGPT for the manual comparison.
# ============================================================

CASE_PROMPTS: Dict[str, str] = {
    "case1_unequal_variance_anova":
        "Compare the `outcome` values across the three `treatment` groups. "
        "Is there a statistically significant difference between the groups? "
        "Report everything I would need for a rigorous write-up.",
    "case2_high_leverage_regression":
        "Fit a regression predicting `exam_score` from `hours_studied` and tell me "
        "about the relationship, including anything I should be careful about.",
    "case3_multiple_comparisons":
        "For each of metric_1, metric_2, metric_3, metric_4, and metric_5, "
        "compare the control group against the variant group. Which metrics differ?",
    "case4_nonnormal_two_group":
        "Compare `response_time` between the placebo and drug arms and tell me "
        "whether the difference is statistically significant.",
    "case5_paired_nonnormal":
        "These are the same subjects measured before (`pre_score`) and after "
        "(`post_score`) an intervention. Did their scores change significantly?",
    "case6_effect_size_reporting":
        "Compare `test_score` between the 2024 and 2025 cohorts and tell me how "
        "big the difference is, not just whether it is significant.",
}


# ============================================================
# Driving the graph
# ============================================================

def _make_workspace(case_key: str) -> str:
    ws = tempfile.mkdtemp(prefix=f"e2e_{case_key}_")
    # build_context_node discovers any file starting with "working_data" to
    # build the dataset profile (it reads CSV fine). But plugin execution uses
    # AgentContext.load_df(), whose fallback path reads working_data.PARQUET.
    # So we must provide BOTH formats or the LLM will see the columns yet every
    # tool will fail with "data file not found".
    src_csv = os.path.join(DATASETS_DIR, f"{RUBRICS[case_key]['dataset']}.csv")
    src_pq = os.path.join(DATASETS_DIR, f"{RUBRICS[case_key]['dataset']}.parquet")
    shutil.copy(src_csv, os.path.join(ws, "working_data.csv"))
    shutil.copy(src_pq, os.path.join(ws, "working_data.parquet"))
    return ws


def _run_full_agent(case_key: str, prompt: str) -> Dict[str, Any]:
    """
    Drive the compiled LangGraph app to completion for one prompt.

    Returns the final state dict (which carries analysis_runs, observations,
    the final report, etc.).
    """
    # Import lazily so that --help / no-key paths don't import heavy deps.
    from core.graph import app as graph_app

    workspace = _make_workspace(case_key)
    thread_id = f"bench_{case_key}_{uuid.uuid4().hex[:6]}"
    config = {"configurable": {"thread_id": thread_id}}

    initial_state = {
        "user_request": prompt,
        "workspace_dir": workspace,
        "current_step": 0,
        "max_steps": MAX_STEPS,
        "observations": [],
        "analysis_runs": [],
    }

    # The graph interrupts before human_review. For a benchmark we auto-approve
    # any pending action and resume until the graph reaches END.
    state = initial_state
    guard = 0
    final_state: Dict[str, Any] = {}

    stream = graph_app.stream(state, config=config)
    while True:
        guard += 1
        if guard > 50:
            print(f"  [WARN] {case_key}: exceeded resume guard; stopping.")
            break

        try:
            for event in stream:
                # event is {node_name: state_delta}; keep the latest snapshot
                for node_name, node_state in event.items():
                    if isinstance(node_state, dict):
                        final_state.update(node_state)
        except Exception as exc:
            print(f"  [ERROR] {case_key}: graph raised {type(exc).__name__}: {exc}")
            break

        # Check whether we interrupted before human_review
        snapshot = graph_app.get_state(config)
        if not snapshot.next:
            # No pending node -> graph finished
            break

        if "human_review" in snapshot.next:
            # Auto-approve, mirroring the real UI's Approve button:
            # the graph routes to `execute` only when current_verification.status
            # == "allowed". We must flip that field, NOT invent new keys.
            vr = snapshot.values.get("current_verification")
            if vr is None:
                print(f"  [WARN] {case_key}: human_review with no current_verification; stopping.")
                break

            if hasattr(vr, "model_copy"):
                vr = vr.model_copy(update={"status": "allowed"})
            elif hasattr(vr, "copy"):
                vr = vr.copy(update={"status": "allowed"})
            elif isinstance(vr, dict):
                vr = {**vr, "status": "allowed"}
            else:
                vr.status = "allowed"

            graph_app.update_state(
                config,
                {
                    "current_verification": vr,
                    "human_review_required": False,
                },
            )
            stream = graph_app.stream(None, config=config)
            continue

        # Some other interrupt we didn't expect -> resume once
        stream = graph_app.stream(None, config=config)

    # Pull the authoritative final state from the checkpointer
    snapshot = graph_app.get_state(config)
    if snapshot and snapshot.values:
        final_state = dict(snapshot.values)

    final_state["_workspace"] = workspace
    return final_state


# ============================================================
# Scoring (reuses the same detectors as the direct runner, but reads from
# the agent's collected analysis_runs)
# ============================================================

def _all_run_metrics_blob(state: Dict[str, Any]) -> str:
    runs = state.get("analysis_runs", []) or []
    obs = state.get("observations", []) or []
    return json.dumps({"analysis_runs": runs, "observations": obs}, default=str).lower()


def _tools_used(state: Dict[str, Any]) -> List[str]:
    runs = state.get("analysis_runs", []) or []
    return [r.get("tool_name") for r in runs if isinstance(r, dict)]


def _score_case(case_key: str, state: Dict[str, Any]):
    """
    Apply the rubric using the agent's collected runs. We detect rigor items
    from the union of all analysis_runs' metrics + the final report text.
    """
    blob = _all_run_metrics_blob(state)
    tools = _tools_used(state)
    rubric = RUBRICS[case_key]
    hits = set()

    # Generic detectors keyed by rigor item key. These mirror the direct-runner
    # detectors but operate on the whole-session blob, since with the LLM in the
    # loop we don't control exactly which tool/fields appear.
    def has(*subs: str) -> bool:
        return any(s in blob for s in subs)

    for item in rubric["rigor_items"]:
        k = item.key
        if k == "levene_or_variance_check" and has("levene", "homogeneity", "equal_variance"):
            hits.add(k)
        elif k == "variance_robust_anova_used" and has("welch", "alexander", "govern"):
            hits.add(k)
        elif k == "games_howell_posthoc" and has("games", "howell"):
            hits.add(k)
        elif k == "effect_size_reported" and has("eta_squared", "epsilon_squared", "omega_squared", "cohens_d", "effect_size", "hedges"):
            hits.add(k)
        elif k == "influence_diagnostics" and has("cook", "leverage", "dffits", "influence"):
            hits.add(k)
        elif k == "flags_the_outlier" and has("high_cook", "high_leverage", "influential", "outlier"):
            hits.add(k)
        elif k == "residual_normality" and has("shapiro", "jarque", "residual_normal"):
            hits.add(k)
        elif k == "heteroscedasticity_check" and has("breusch", "pagan", "hc3", "robust", "heterosced"):
            hits.add(k)
        elif k == "r_squared_reported" and has("r_squared", "r-squared"):
            hits.add(k)
        elif k == "multiple_comparison_warning" and has("family-wise", "familywise", "multiple compar", "fwer"):
            hits.add(k)
        elif k == "correction_suggested" and has("bonferroni", "benjamini", "hochberg", "fdr"):
            hits.add(k)
        elif k == "per_test_effect_size" and has("cohens_d", "effect_size", "hedges"):
            hits.add(k)
        elif k == "no_false_discovery_overclaim":
            hits.add(k)  # neutral reporting; credited unless evidence of overclaim
        elif k == "normality_check" and has("shapiro", "normal"):
            hits.add(k)
        elif k == "nonparametric_recommended" and has("mann-whitney", "mann whitney", "kruskal"):
            hits.add(k)
        elif k == "rank_effect_size" and has("rank-biserial", "rank_biserial", "hodges", "epsilon_squared"):
            hits.add(k)
        elif k == "does_not_blindly_ttest" and ("nonparametric_group_comparison" in tools):
            hits.add(k)
        elif k == "treats_as_paired" and ("paired_comparison" in tools):
            hits.add(k)
        elif k == "difference_normality" and has("shapiro") and has("differ", "diff"):
            hits.add(k)
        elif k == "wilcoxon_recommended" and has("wilcoxon"):
            hits.add(k)
        elif k == "paired_effect_size" and has("cohens_d_z", "rank_biserial", "rank-biserial"):
            hits.add(k)
        elif k == "effect_size_ci" and has("ci_low", "ci_high", "confidence interval", "95% ci"):
            hits.add(k)
        elif k == "magnitude_label" and has("magnitude", "small", "medium", "large", "negligible"):
            hits.add(k)
        elif k == "hedges_correction" and has("hedges"):
            hits.add(k)
        elif k == "mean_diff_ci" and has("mean_difference_ci", "mean difference"):
            hits.add(k)

    return hits


def main():
    print("=" * 72)
    print("END-TO-END BENCHMARK: FULL AGENT (LLM routing + deterministic stats)")
    print("=" * 72)

    if not _require_api_key():
        sys.exit(1)

    only = sys.argv[1] if len(sys.argv) > 1 else None
    cases = [only] if only else list(CASE_PROMPTS.keys())

    results = {}
    total_hit = 0
    total_possible = 0

    for case_key in cases:
        if case_key not in CASE_PROMPTS:
            print(f"Unknown case: {case_key}")
            continue

        prompt = CASE_PROMPTS[case_key]
        rubric = RUBRICS[case_key]
        print(f"\n[{case_key}]  {rubric['title']}")
        print(f"  Prompt: {prompt[:80]}...")

        state = _run_full_agent(case_key, prompt)
        hits = _score_case(case_key, state)

        items = rubric["rigor_items"]
        n_total = len(items)
        n_hit = sum(1 for it in items if it.key in hits)
        total_hit += n_hit
        total_possible += n_total

        print(f"  Tools used: {_tools_used(state)}")
        print(f"  Score: {n_hit}/{n_total}")
        for it in items:
            mark = "PASS" if it.key in hits else "MISS"
            print(f"    [{mark}] {it.label}")

        results[case_key] = {
            "title": rubric["title"],
            "prompt": prompt,
            "tools_used": _tools_used(state),
            "score": n_hit,
            "total": n_total,
            "items": [
                {"key": it.key, "label": it.label, "passed": it.key in hits}
                for it in items
            ],
        }

        # Clean up workspace
        ws = state.get("_workspace")
        if ws and os.path.isdir(ws):
            shutil.rmtree(ws, ignore_errors=True)

    print("\n" + "=" * 72)
    if total_possible:
        print(f"OVERALL: {total_hit}/{total_possible} rigor items "
              f"({100*total_hit/total_possible:.0f}%)")
    print("=" * 72)

    out_path = os.path.join(RESULTS_DIR, "our_agent_e2e_results.json")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump({
            "product": "our_agent_end_to_end",
            "note": "Full LangGraph pipeline incl. LLM routing (gpt-4o).",
            "overall_hit": total_hit,
            "overall_possible": total_possible,
            "cases": results,
        }, f, indent=2, default=str)
    print(f"\nResults written to: {out_path}")


if __name__ == "__main__":
    main()
